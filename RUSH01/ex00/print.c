#include <unistd.h>

void	print(int tab[4][4])
{
	int		i;
	int		j;
	char	tower;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			tower = tab[i][j] + '0';
			write(1, &tower, 1);
			if (j < 3)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
